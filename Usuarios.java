    private void Consultar() { 
String sql = "select *from Usuarios where ID =?";/*para chmar o estudante 2*/
    try {
        pst=conexao.prepareStatement(sql);
         pst.setString(1,txtID.getText());
           rs=pst.executeQuery();
         if (rs.next()) {
            txtNomeUsuario.setText(rs.getString(2));
             txtSenha.setText(rs.getString(3));
             
         }else{
             JOptionPane.showMessageDialog(null, "Usuario nÃ£o encontrado");
             txtNomeUsuario.setText(null);
             txtSenha.setText(null);
                   
         }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }
    private void Adicionar(){
String sql = "insert into Usuarios(ID, NomeUsuario, Senha) values( ?,?, ?)";
try {
    
    pst = conexao.prepareStatement(sql);
    pst.setString(1,txtID.getText());
    pst.setString(2,txtNomeUsuario.getText());
    pst.setString(3,txtSenha.getText());
  
    /*Actualizar a table com o dados do formulario*/
  int Adicionado =  pst.executeUpdate();
     if (Adicionado >0) {
         JOptionPane.showMessageDialog(null, "Usuario adicionado com Sucesso");
        
     }
    } catch (Exception e) {
    JOptionPane.showMessageDialog(null, sql);
    }
     
    
    }
